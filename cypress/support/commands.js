import Sendsay from 'sendsay-api';

Cypress.Commands.add('login', ({ login, password }) => {
	const sendsay = new Sendsay();

  cy.wrap(sendsay.login({ login, password }).then(() => { cy.setCookie('sendsay_session', sendsay.session) }));
});



